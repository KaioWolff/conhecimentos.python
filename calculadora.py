def soma(n1, n2):
    return n1+n2

def subtracao(n1,n2):
    return n1-n2

def multiplicacao(n1,n2):
    return n1*n2

def divisao(n1,n2):
    return n1/n2

def exponencial(n1,n2):
    return n1**n2

def percentual(n1,n2):
    return n1/100*n2

def fatorial(n2):
    #multi = 1
    for multi in range(1, n2 + 1):
        fatorial = multi * n2
    return fatorial